package model;

/**
 *
 * @author ADMIN
 */
public class OrderHistory {
    private int id;
    private String price;
    private String date,note;

    public OrderHistory() {
    }

    public OrderHistory(int id, String price, String date, String note) {
        this.id = id;
        this.price = price;
        this.date = date;
        this.note = note;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    @Override
    public String toString() {
        return "OrderHistory{" + "id=" + id + ", price=" + price + ", date=" + date + ", note=" + note + '}';
    }
    
    
    
}
